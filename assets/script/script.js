//VARIABLES
let currentQuestion; // Current question
let correctAnswer; // Currect answer to current question
let incorrectAnswer; // Keeps track of incorrect answers for scoreboard
let unanswered; // Keeps track of unanswered questions for scoreboard
let secondsLeft; // Remaining seconds on timer 
let timeInterval; // Time interval given to answer question
let answered; 
let playerSelected; // Player's answer choice

//MESSAGES
let messages = {
	correct: "Nice job, you are correct!",
	incorrect: "Good guess but not quite right.",
	endtimeInterval: "Sorry, you are out of time!",
	finished: "Well done!  Let's see how you did."
}
// STARTS GAME
$(document).ready(function(){
	$(".questionPage").hide();
	$(".answerPage").hide();
	$(".scoreboard").hide();
}); 
	
$('#startBtn').on('click', function(){
	$(this).hide();
	$('#instructionsPage').hide();
	$(".questionPage").show();
	newGame();
});

//RESTARTS THE GAME
$('#startOverBtn').on('click', function(){
	$(this).hide();
	$(".scoreboard").hide();
	newGame();
});

function newGame(){
	$('#finalMessage').empty();
	$('#correctAnswers').empty();
	$('#incorrectAnswers').empty();
	$('#unanswered').empty();
	currentQuestion = 0;
	correctAnswer = 0;
	incorrectAnswer = 0;
	unanswered = 0;
	newQuestion();
}
//SET UP NEW Q&As
function newQuestion(){
	$(".answerPage").hide();
	$(".questionPage").show();
	$('#message').empty();
	$('#correctedAnswer').empty();
	$('#jpg').empty();
	$('#jpgCredit').empty();
	$('#moreInfo').empty();

	answered = true;
	
	$('#currentQuestion').html('Question '+(currentQuestion+1)+' of '+triviaQuestions.length);
	$('.question').html(triviaQuestions[currentQuestion].question);
	for(let i = 0; i < 4; i++){
		let answerChoices = $('<div>');
		answerChoices.text(triviaQuestions[currentQuestion].answerList[i]);
		answerChoices.attr({'data-index': i });
		answerChoices.addClass('thisChoice');
		$('.answerList').append(answerChoices);
	}
	countdown();
	//SELECTING ANSWER PAUSES TIMER AND TRIGGERS ANSWER PAGE
	$('.thisChoice').on('click',function(){
		playerSelected = $(this).data('index');
		clearInterval(timeInterval);
		$('.questionPage').hide();
		$(".answerPage").show();
		answerPage();
	});
}
// TIMER FUNCTIONS
//sets timer to count down
function countdown(){
	secondsLeft = 20;
	$('#timeRemaining').html('Time Remaining: ' + secondsLeft);
	answered = true;
	timeInterval = setInterval(showCountdown, 1000);
}
//shows countown and clears interval
function showCountdown(){
	secondsLeft--;
	$('#timeRemaining').html('Time Remaining: ' + secondsLeft);
	if(secondsLeft < 1){
		clearInterval(timeInterval);
		answered = false;
		answerPage();
	}
}
//DISPLAYS ANSWERS AND IMAGES
function answerPage(){
	$('#currentQuestion').empty();
	$('.thisChoice').empty();  
	$('.question').empty();
	$(".answerPage").show();
	$(".questionPage").hide();

	let rightAnswerText = triviaQuestions[currentQuestion].answerList[triviaQuestions[currentQuestion].answer];
	let rightAnswerIndex = triviaQuestions[currentQuestion].answer;
	$('#jpg').html('<img src = "assets/images/'+ jpgArray[currentQuestion] +'.jpg" style="max-width: 100%;	height: auto; max-height: 85vh">');  
	$('#jpgCredit').html(creditArray[currentQuestion]);
	$('#moreInfo').html(moreInfoArray[currentQuestion]);

	//checks to see correct, incorrect, or unanswered
	if((playerSelected == rightAnswerIndex) && (answered == true)){
		correctAnswer++;
		$('#message').html(messages.correct);
	} else if((playerSelected != rightAnswerIndex) && (answered == true)){
		incorrectAnswer++;
		$('#message').html(messages.incorrect);
		$('#correctedAnswer').html('The correct answer was  ' + rightAnswerText  + '.');
	} else{
		unanswered++;
		$('#message').html(messages.endtimeInterval);
		$('#correctedAnswer').html('The correct answer was  ' + rightAnswerText  + '.');
		answered = true;
	}
	if(currentQuestion == (triviaQuestions.length-1)){
		setTimeout(scoreboard, 5000)
	} else{
		currentQuestion++;
		setTimeout(newQuestion, 5000);
	}	
}
// KEEPS TRACK OF CORRECT AND INCORRECT ANSWERS
function scoreboard(){
	$('#timeRemaining').empty();
	$('#message').empty();
	$('#correctedAnswer').empty();

	$(".answerPage").hide();
	$(".scoreboard").hide();
	$(".scoreboard").show();
	$('#finalMessage').html(messages.finished);
	$('#correctAnswers').html("Correct Answers: " + correctAnswer);
	$('#incorrectAnswers').html("Incorrect Answers: " + incorrectAnswer);
	$('#unanswered').html("Unanswered: " + unanswered);
	$('#startOverBtn').addClass('reset');
	$('#startOverBtn').show();
	// $('#startOverBtn').html('Start Over?');
}
